use bedrock_protocol_schema::Schema;

use crate::naming;

/// Generate the MinecraftPackets factory using numeric packet IDs (more robust).
pub fn generate_with_ids(schema: &Schema) -> String {
    let mut output = String::new();
    output.push_str("// This file is @generated by bedrock-codegen. Do not edit.\n\n");
    output.push_str("#![allow(non_camel_case_types, unused_imports, unused_must_use, unused_variables)]\n\n");
    output.push_str("use crate::{MinecraftPacketIds, Packet};\n");
    output.push_str("use super::gen_packets::*;\n\n");

    output.push_str("pub struct MinecraftPackets;\n\n");
    output.push_str("impl MinecraftPackets {\n");
    output.push_str("    pub fn create_packet(id: MinecraftPacketIds) -> Box<dyn Packet> {\n");
    output.push_str("        match id as i32 {\n");

    for def in &schema.packets {
        let struct_name = naming::to_rust_struct_name(&def.name);
        output.push_str(&format!(
            "            {} => Box::new({}::default()),\n",
            def.id, struct_name
        ));
    }

    output.push_str("            _ => Box::new(crate::UnimplementedPacket::new(id as i32)),\n");
    output.push_str("        }\n");
    output.push_str("    }\n");
    output.push_str("}\n");

    output
}

/// Generate the factory using enum variant names (requires exact variant name matching).
pub fn generate(schema: &Schema) -> String {
    generate_with_ids(schema)
}
