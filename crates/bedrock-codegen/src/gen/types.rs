use bedrock_protocol_schema::{
    field::{FieldType, SwitchCaseBranch},
    Schema, TypeDefinition,
};

use crate::naming;
use crate::type_mapping;

/// Generate Rust source code for all type definitions.
pub fn generate(types: &[TypeDefinition], schema: &Schema) -> String {
    let mut output = String::new();
    output.push_str("// This file is @generated by bedrock-codegen. Do not edit.\n\n");
    output.push_str("#![allow(non_camel_case_types, unused_imports, unused_must_use, unused_variables)]\n\n");
    output.push_str("use bedrock_common::{BedrockRead, BedrockWrite, BedrockSerializable, BinaryStreamError};\n");
    output.push_str("use bedrock_binary_stream::{BinaryStream, ReadOnlyBinaryStream};\n");
    output.push_str("use crate::types::*;\n");
    output.push_str("use crate::gen_enums::*;\n\n");

    for def in types {
        if is_hand_implemented(&def.name) { continue; }
        if has_invalid_rust_name(&def.name) { continue; }
        if has_template_syntax(&def.name) { continue; }

        match generate_type(def, schema) {
            Ok(code) => { output.push_str(&code); output.push('\n'); }
            Err(_msg) => {
                // Type generation failed — emit a Vec<u8> newtype stub instead
                let struct_name = naming::to_rust_struct_name(&def.name);
                output.push_str(&format!(
                    "#[derive(Debug, Clone, PartialEq, Default)]\n\
                     pub struct {}(pub Vec<u8>);\n\n\
                     impl BedrockSerializable for {} {{\n\
                     fn write_to(&self, stream: &mut dyn BedrockWrite) -> Result<(), BinaryStreamError> {{\n\
                         stream.write_raw_bytes(&self.0)\n\
                     }}\n\
                     fn read_from(stream: &mut dyn BedrockRead) -> Result<Self, BinaryStreamError> {{\n\
                         Ok(Self(stream.read_remaining()?))\n\
                     }}\n\
                     }}\n\n",
                    struct_name, struct_name
                ));
            }
        }
    }

    output
}

fn is_hand_implemented(name: &str) -> bool {
    // Types hand-written in bedrock-protocol/src/types/
    matches!(name,
        "Vec3" | "BlockPos" | "mce::UUID" | "FullContainerName"
        | "ItemData" | "ItemStackRequest" | "ItemStackRequestAction"
        | "ItemStackRequestActionTransferBase" | "ItemStackRequestActionType"
        | "ItemStackRequestData" | "ItemStackRequestSlotInfo"
    )
}

fn has_invalid_rust_name(name: &str) -> bool {
    if name.is_empty() { return true; }
    if name.starts_with(|c: char| c.is_numeric() || c == '_') { return true; }
    if name.contains(',') { return true; }
    if name.contains('<') || name.contains('>') { return true; }
    if name.contains('(') || name.contains(')') { return true; }
    false
}

fn has_template_syntax(name: &str) -> bool {
    name.contains("struct ") || name.contains("int32_t") || name.contains("int64_t")
}

/// Generate a single type struct + BedrockSerializable impl.
fn generate_type(def: &TypeDefinition, schema: &Schema) -> Result<String, String> {
    let struct_name = naming::to_rust_struct_name(&def.name);

    if def.fields.is_empty() {
        return Err("no fields".to_string());
    }

    let mut out = String::new();

    out.push_str(&format!("#[derive(Debug, Clone, PartialEq, Default)]\n"));
    out.push_str(&format!("pub struct {} {{\n", struct_name));

    for field in &def.fields {
        let field_name = naming::to_snake_case(&field.name);
        let field_type = resolve_field_type(field, schema)?;
        out.push_str(&format!("    pub {}: {},\n", field_name, field_type));
    }
    out.push_str("}\n\n");

    out.push_str(&format!("impl BedrockSerializable for {} {{\n", struct_name));

    // write_to
    out.push_str("    fn write_to(&self, stream: &mut dyn BedrockWrite) -> Result<(), BinaryStreamError> {\n");
    for field in &def.fields {
        let field_name = naming::to_snake_case(&field.name);
        out.push_str(&format!("        {}", write_field(&field_name, field, schema)?));
    }
    out.push_str("        Ok(())\n");
    out.push_str("    }\n\n");

    // read_from
    out.push_str("    fn read_from(stream: &mut dyn BedrockRead) -> Result<Self, BinaryStreamError> {\n");
    out.push_str("        Ok(Self {\n");
    for field in &def.fields {
        let field_name = naming::to_snake_case(&field.name);
        out.push_str(&format!("            {}: {},\n", field_name, read_field(field, schema)?));
    }
    out.push_str("        })\n");
    out.push_str("    }\n}\n");

    Ok(out)
}

fn resolve_field_type(field: &bedrock_protocol_schema::FieldDefinition, schema: &Schema) -> Result<String, String> {
    let inner = inner_type(&field.field_type, &field.enum_ref, schema)?;
    if field.repeat.is_some() || field.type_repeat.is_some() {
        return Ok(format!("Vec<{}>", inner));
    }
    Ok(inner)
}

fn inner_type(field_type: &FieldType, enum_ref: &Option<String>, schema: &Schema) -> Result<String, String> {
    match field_type {
        FieldType::Named(name) => {
            if let Some(enum_name) = enum_ref {
                return Ok(naming::to_rust_enum_name(enum_name));
            }
            if type_mapping::is_integer_alias(name) {
                return Ok(type_mapping::integer_alias_type(name).to_string());
            }
            let prim = type_mapping::rust_primitive_type(name);
            if prim != "Vec<u8>" || name == "raw_bytes" {
                return Ok(prim.to_string());
            }
            if name == "CompoundTag" {
                return Ok("Vec<u8> /* CompoundTag */".to_string());
            }
            let struct_name = naming::to_rust_struct_name(name);
            if has_invalid_rust_name(&struct_name) || has_template_syntax(name) {
                return Err(format!("invalid type name: {}", name));
            }
            // Type not defined in schema and not a primitive — treat as raw bytes
            if !schema.is_type(name) && !schema.is_enum(name) {
                return Ok("Vec<u8>".to_string());
            }
            // Even if type is in schema, it may have been skipped during generation
            // (e.g. original name contains '<' or '(' which fails has_invalid_rust_name)
            if name.contains('<') || name.contains('>') || name.contains(',') || name.contains('(') || name.contains(')') {
                return Ok("Vec<u8>".to_string());
            }
            Ok(struct_name)
        }
        FieldType::SwitchCase(sc) => {
            let has_empty = sc.cases.iter().any(|c| matches!(c, SwitchCaseBranch::Empty));
            if has_empty && sc.cases.len() == 2 {
                let non_empty = sc.cases.iter().find(|c| !matches!(c, SwitchCaseBranch::Empty));
                if let Some(branch) = non_empty {
                    match branch {
                        SwitchCaseBranch::Primitive(s) => {
                            return Ok(format!("Option<{}>", type_mapping::rust_primitive_type(s)));
                        }
                        SwitchCaseBranch::Type(s) => {
                            return Ok(format!("Option<{}>", naming::to_rust_struct_name(s)));
                        }
                        _ => return Err("unsupported switch/case branch".to_string()),
                    }
                }
            }
            Err("unsupported switch/case (non-trivial)".to_string())
        }
        FieldType::Map { key, value } => {
            let kt = type_mapping::rust_primitive_type(key);
            let vt = type_mapping::rust_primitive_type(value);
            // f32 doesn't implement Hash/Eq, can't be HashMap key
            if kt == "f32" || kt == "f64" {
                return Err("Map with float key is not supported".to_string());
            }
            Ok(format!("std::collections::HashMap<{}, {}>", kt, vt))
        }
    }
}

fn write_field(field_name: &str, field: &bedrock_protocol_schema::FieldDefinition, schema: &Schema) -> Result<String, String> {
    let self_field = format!("self.{}", field_name);
    if let Some(ref ri) = field.repeat {
        if ri.prefix.is_some() {
            return Ok(format!(
                "stream.write_unsigned_varint({}.len() as u32)?;\n        \
                 for item in &{} {{\n            {}\n        }}\n",
                self_field, self_field, write_single("item", &field.field_type, &field.enum_ref, schema)?));
        } else if ri.count.is_some() {
            return Ok(format!(
                "for item in &{} {{\n            {}\n        }}\n",
                self_field, write_single("item", &field.field_type, &field.enum_ref, schema)?));
        }
    }
    if let Some(ref tr) = field.type_repeat {
        if tr.prefix.is_some() {
            return Ok(format!(
                "stream.write_unsigned_varint({}.len() as u32)?;\n        \
                 for item in &{} {{\n            {}\n        }}\n",
                self_field, self_field, write_single("item", &field.field_type, &field.enum_ref, schema)?));
        }
    }
    write_single(&self_field, &field.field_type, &field.enum_ref, schema)
}

fn write_single(value_expr: &str, field_type: &FieldType, enum_ref: &Option<String>, schema: &Schema) -> Result<String, String> {
    let is_ref = value_expr == "item";
    match field_type {
        FieldType::Named(name) => {
            if enum_ref.is_some() {
                let v = if is_ref { format!("*{} as i32", value_expr) } else { format!("{} as i32", value_expr) };
                return Ok(format!("stream.write_varint({})?;", v));
            }
            if type_mapping::is_integer_alias(name) {
                let ty = type_mapping::integer_alias_type(name);
                let v = if is_ref { format!("*{}", value_expr) } else { value_expr.to_string() };
                return if ty == "i64" {
                    Ok(format!("stream.write_varint64({})?;", v))
                } else {
                    Ok(format!("stream.write_varint({})?;", v))
                };
            }
            if let Some(m) = type_mapping::primitive_mapping(name) {
                if m.write_call == "write_string" {
                    return Ok(format!("stream.write_string({})?;", format!("&{}", value_expr)));
                }
                let v = if is_ref { format!("*{}", value_expr) } else { value_expr.to_string() };
                return Ok(format!("stream.{}({})?;", m.write_call, v));
            }
            if name == "CompoundTag" {
                return Ok(format!("stream.write_raw_bytes(&{});", value_expr));
            }
            // Unknown type — treat as raw bytes
            if !schema.is_type(name) && !schema.is_enum(name) {
                return Ok(format!("stream.write_raw_bytes(&{});", value_expr));
            }
            Ok(format!("{}.write_to(stream)?;", value_expr))
        }
        FieldType::SwitchCase(sc) => {
            let has_empty = sc.cases.iter().any(|c| matches!(c, SwitchCaseBranch::Empty));
            if has_empty {
                return Ok(format!(
                    "if let Some(ref val) = {} {{\n            \
                     stream.write_bool(true)?;\n        \
                     }} else {{\n            \
                     stream.write_bool(false)?;\n        \
                     }}\n", value_expr));
            }
            Err("complex switch/case write not supported".to_string())
        }
        FieldType::Map { .. } => {
            Err("map write not supported".to_string())
        }
    }
}

fn read_field(field: &bedrock_protocol_schema::FieldDefinition, schema: &Schema) -> Result<String, String> {
    if let Some(ref ri) = field.repeat {
        if ri.prefix.is_some() {
            let inner = read_single(&field.field_type, &field.enum_ref, schema)?;
            return Ok(format!(
                "{{\n            \
                 let mut items: Vec<_> = Vec::new();\n            \
                 let _count = stream.read_unsigned_varint()?;\n            \
                 for _ in 0.._count {{\n                \
                     let item = {};\n                \
                     items.push(item);\n            \
                 }}\n            \
                 items\n        }}", inner));
        } else if let Some(c) = ri.count {
            let inner = read_single(&field.field_type, &field.enum_ref, schema)?;
            return Ok(format!(
                "{{\n            \
                 let mut items = Vec::with_capacity({});\n            \
                 for _ in 0..{} {{\n                \
                     let item = {};\n                \
                     items.push(item);\n            \
                 }}\n            \
                 items\n        }}", c, c, inner));
        }
    }
    if let Some(ref tr) = field.type_repeat {
        if tr.prefix.is_some() {
            let inner = read_single(&field.field_type, &field.enum_ref, schema)?;
            return Ok(format!(
                "{{\n            \
                 let mut items: Vec<_> = Vec::new();\n            \
                 let _count = stream.read_unsigned_varint()?;\n            \
                 for _ in 0.._count {{\n                \
                     let item = {};\n                \
                     items.push(item);\n            \
                 }}\n            \
                 items\n        }}", inner));
        }
    }
    read_single(&field.field_type, &field.enum_ref, schema)
}

fn read_single(field_type: &FieldType, enum_ref: &Option<String>, schema: &Schema) -> Result<String, String> {
    match field_type {
        FieldType::Named(name) => {
            if let Some(enum_name) = enum_ref {
                let enum_type = naming::to_rust_enum_name(enum_name);
                return Ok(format!("<{}>::try_from(stream.read_varint()?)?", enum_type));
            }
            if type_mapping::is_integer_alias(name) {
                let ty = type_mapping::integer_alias_type(name);
                return Ok(if ty == "i64" {
                    "stream.read_varint64()?".to_string()
                } else {
                    "stream.read_varint()?".to_string()
                });
            }
            if name == "CompoundTag" {
                return Ok("stream.read_remaining()? /* CompoundTag */".to_string());
            }
            if let Some(m) = type_mapping::primitive_mapping(name) {
                return Ok(format!("{}?", m.read_call));
            }
            let struct_name = naming::to_rust_struct_name(name);
            if has_invalid_rust_name(&struct_name) {
                return Err(format!("invalid type: {}", name));
            }
            // Original name has invalid Rust chars — treat as raw bytes
            if name.contains('<') || name.contains('>') || name.contains('(') || name.contains(')') {
                return Ok("stream.read_remaining()?".to_string());
            }
            if schema.is_enum(name) {
                return Ok(format!("<{}>::try_from(stream.read_varint()?)?", struct_name));
            }
            if schema.is_type(name) {
                return Ok(format!("<{} as BedrockSerializable>::read_from(stream)?", struct_name));
            }
            Ok("stream.read_remaining()?".to_string())
        }
        FieldType::SwitchCase(sc) => {
            let has_empty = sc.cases.iter().any(|c| matches!(c, SwitchCaseBranch::Empty));
            if has_empty {
                return Ok(format!(
                    "{{\n            \
                     if stream.read_bool()? {{\n                \
                         Some(stream.read_unsigned_varint()? as i32)\n            \
                     }} else {{\n                \
                         None\n            \
                     }}\n        }}"));
            }
            Ok("None /* switch/case */".to_string())
        }
        FieldType::Map { key, value } => {
            Ok(format!(
                "{{\n            \
                 let mut map = std::collections::HashMap::new();\n            \
                 let _count = stream.read_unsigned_varint()?;\n            \
                 for _ in 0.._count {{\n                \
                     let k = stream.{}()?;\n                \
                     let v = stream.{}()?;\n                \
                     map.insert(k, v);\n            \
                 }}\n            \
                 map\n        }}",
                type_mapping::read_method(key),
                type_mapping::read_method(value)))
        }
    }
}
