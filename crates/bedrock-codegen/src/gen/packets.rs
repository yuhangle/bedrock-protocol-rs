use bedrock_protocol_schema::{
    field::FieldType,
    Schema,
};

use crate::naming;
use crate::type_mapping;

/// Generate Rust source code for all packet definitions.
pub fn generate(schema: &Schema) -> String {
    let mut output = String::new();
    output.push_str("// This file is @generated by bedrock-codegen. Do not edit.\n\n");
    output.push_str("#![allow(non_camel_case_types, unused_imports, unused_must_use, unused_variables)]\n\n");
    output.push_str("use bedrock_common::{BedrockRead, BedrockWrite, BedrockSerializable, BinaryStreamError};\n");
    output.push_str("use bedrock_binary_stream::{BinaryStream, ReadOnlyBinaryStream};\n");
    output.push_str("use crate::Packet;\n");
    output.push_str("use crate::gen_packet_ids::MinecraftPacketIds;\n");
    output.push_str("use crate::types::*;\n");
    output.push_str("use crate::gen_enums::*;\n");
    output.push_str("use crate::gen_types::*;\n");
    output.push_str("use crate::gen_stubs::*;\n\n");

    for def in &schema.packets {
        output.push_str(&generate_packet(def, schema));
        output.push('\n');
    }

    output
}

fn generate_packet(
    def: &bedrock_protocol_schema::PacketDefinition,
    schema: &Schema,
) -> String {
    let struct_name = naming::to_rust_struct_name(&def.name);
    let packet_id_name = enum_name_from_packet_id(&def.name, def.id);

    let mut out = String::new();

    out.push_str(&format!("#[derive(Debug, Clone, PartialEq)]\n"));
    out.push_str(&format!("pub struct {} {{\n", struct_name));

    for field in &def.fields {
        let field_name = naming::to_snake_case(&field.name);
        let field_type = resolve_field_rust_type(field, schema);
        out.push_str(&format!("    pub {}: {},\n", field_name, field_type));
    }

    if def.fields.is_empty() {
        out.push_str("    _private: (),\n");
    }
    out.push_str("}\n\n");

    // Default impl
    out.push_str(&format!("impl Default for {} {{\n", struct_name));
    out.push_str("    fn default() -> Self {\n        Self {\n");
    for field in &def.fields {
        let field_name = naming::to_snake_case(&field.name);
        out.push_str(&format!("            {}: {},\n", field_name, field_default_value(field, schema)));
    }
    if def.fields.is_empty() {
        out.push_str("            _private: (),\n");
    }
    out.push_str("        }\n    }\n}\n\n");

    // Packet impl
    out.push_str(&format!("impl Packet for {} {{\n", struct_name));
    out.push_str(&format!("    fn packet_id(&self) -> MinecraftPacketIds {{\n"));
    out.push_str(&format!("        MinecraftPacketIds::{}\n", packet_id_name));
    out.push_str("    }\n\n");
    out.push_str(&format!("    fn packet_name(&self) -> &'static str {{\n"));
    out.push_str(&format!("        \"{}\"\n", def.name));
    out.push_str("    }\n}\n\n");

    // BedrockSerializable impl
    out.push_str(&format!("impl BedrockSerializable for {} {{\n", struct_name));

    // write_to
    out.push_str("    fn write_to(&self, stream: &mut dyn BedrockWrite) -> Result<(), BinaryStreamError> {\n");
    for field in &def.fields {
        let field_name = naming::to_snake_case(&field.name);
        out.push_str(&format!("        {}", write_field(&field_name, field, schema)));
    }
    out.push_str("        Ok(())\n");
    out.push_str("    }\n\n");

    // read_from
    out.push_str("    fn read_from(stream: &mut dyn BedrockRead) -> Result<Self, BinaryStreamError> {\n");
    out.push_str("        Ok(Self {\n");
    for field in &def.fields {
        let field_name = naming::to_snake_case(&field.name);
        out.push_str(&format!("            {}: {},\n", field_name, read_field(field, schema)));
    }
    if def.fields.is_empty() {
        out.push_str("            _private: (),\n");
    }
    out.push_str("        })\n");
    out.push_str("    }\n}\n");

    out
}

fn enum_name_from_packet_id(name: &str, _id: u32) -> String {
    let stripped = name.strip_suffix("Packet").unwrap_or(name);
    match stripped {
        "Break" => "Break_".to_string(),
        s => s.to_string(),
    }
}

fn resolve_field_rust_type(field: &bedrock_protocol_schema::FieldDefinition, schema: &Schema) -> String {
    let inner = inner_type(&field.field_type, &field.enum_ref, schema);
    if field.repeat.is_some() || field.type_repeat.is_some() {
        return format!("Vec<{}>", inner);
    }
    inner
}

fn inner_type(field_type: &FieldType, enum_ref: &Option<String>, schema: &Schema) -> String {
    match field_type {
        FieldType::Named(name) => {
            if let Some(enum_name) = enum_ref {
                let rust_name = naming::to_rust_enum_name(enum_name);
                if schema.is_enum(enum_name) { return rust_name; }
                return format!("i32 /* enum: {} */", enum_name);
            }
            if type_mapping::is_integer_alias(name) {
                return type_mapping::integer_alias_type(name).to_string();
            }
            let prim = type_mapping::rust_primitive_type(name);
            if prim != "Vec<u8>" || name == "raw_bytes" { return prim.to_string(); }
            if name == "CompoundTag" {
                return "Vec<u8> /* CompoundTag */".to_string();
            }
            let struct_name = naming::to_rust_struct_name(name);
            if schema.is_type(name) || schema.is_enum(name) {
                // Even if the type is in schema, it might have been skipped during
                // generation (e.g., invalid Rust name like "cerealizer<X>::Y")
                if name.contains('<') || name.contains('>') || name.contains('(') || name.contains(')') {
                    return "Vec<u8>".to_string();
                }
                return struct_name;
            }
            // Type not in schema — treat as raw bytes
            "Vec<u8>".to_string()
        }
        FieldType::SwitchCase(sc) => {
            let has_empty = sc.cases.iter().any(|c| matches!(c, bedrock_protocol_schema::field::SwitchCaseBranch::Empty));
            if has_empty {
                if sc.cases.len() == 2 {
                    let non_empty = sc.cases.iter().find(|c| !matches!(c, bedrock_protocol_schema::field::SwitchCaseBranch::Empty));
                    if let Some(branch) = non_empty {
                        let inner_type_name = match branch {
                            bedrock_protocol_schema::field::SwitchCaseBranch::Primitive(s) => {
                                Some(type_mapping::rust_primitive_type(s).to_string())
                            }
                            bedrock_protocol_schema::field::SwitchCaseBranch::Type(s) => {
                                // Check if the type name is valid for Rust
                                if s.contains('<') || s.contains('>') || s.contains('(') || s.contains(')') {
                                    None // invalid, use raw bytes wrapper
                                } else {
                                    Some(naming::to_rust_struct_name(s))
                                }
                            }
                            _ => None,
                        };
                        if let Some(inner) = inner_type_name {
                            return format!("Option<{}>", inner);
                        }
                    }
                }
                return "Option<Vec<u8>>".to_string();
            }
            // Non-empty switch-case (multiple variants): can't auto-generate
            "Vec<u8>".to_string()
        }
        FieldType::Map { key, value } => {
            let kt = type_mapping::rust_primitive_type(key);
            // f32/f64 can't be HashMap keys
            if kt == "f32" || kt == "f64" {
                return "Vec<u8>".to_string();
            }
            // Resolve value type: use struct name if it's a schema type/enum
            let vt = if value.contains('<') || value.contains('>') || value.contains('(') || value.contains(')') {
                "Vec<u8>".to_string()
            } else if schema.is_type(value) || schema.is_enum(value) {
                naming::to_rust_struct_name(value)
            } else {
                type_mapping::rust_primitive_type(value).to_string()
            };
            format!("std::collections::HashMap<{}, {}>", kt, vt)
        }
    }
}

fn field_default_value(field: &bedrock_protocol_schema::FieldDefinition, schema: &Schema) -> String {
    if field.repeat.is_some() || field.type_repeat.is_some() {
        return "Vec::new()".to_string();
    }
    match &field.field_type {
        FieldType::Named(name) => {
            if field.enum_ref.is_some() {
                return "unsafe { std::mem::zeroed() }".to_string();
            }
            if type_mapping::is_integer_alias(name) { return "0".to_string(); }
            // Check for known schema types BEFORE primitive fallback
            if schema.is_type(name) || schema.is_enum(name) {
                if name.contains('<') || name.contains('>') || name.contains('(') || name.contains(')') {
                    return "Vec::new()".to_string();
                }
                if schema.is_enum(name) {
                    return "unsafe { std::mem::zeroed() }".to_string();
                }
                let type_name = naming::to_rust_struct_name(name);
                return format!("{}::default()", type_name);
            }
            match type_mapping::rust_primitive_type(name) {
                "bool" => "false",
                "u8" | "u16" | "u32" | "u64" | "i8" | "i16" | "i32" | "i64" => "0",
                "f32" | "f64" => "0.0",
                "String" => "String::new()",
                "Vec<u8>" => "Vec::new()",
                _ => {
                    if field.enum_ref.is_some() { return "unsafe { std::mem::zeroed() }".to_string(); }
                    return format!("{}::default()", naming::to_rust_struct_name(name));
                }
            }
            .to_string()
        }
        FieldType::SwitchCase(sc) => {
            // SwitchCase with empty branch → Option<T> → default None
            // Without empty branch → Vec<u8> → default Vec::new()
            if sc.cases.iter().any(|c| matches!(c, bedrock_protocol_schema::field::SwitchCaseBranch::Empty)) {
                "None".to_string()
            } else {
                "Vec::new()".to_string()
            }
        }
        FieldType::Map { key, .. } => {
            let kt = type_mapping::rust_primitive_type(key);
            if kt == "f32" || kt == "f64" {
                "Vec::new()".to_string()
            } else {
                "std::collections::HashMap::new()".to_string()
            }
        }
    }
}

fn write_field(field_name: &str, field: &bedrock_protocol_schema::FieldDefinition, schema: &Schema) -> String {
    let self_field = format!("self.{}", field_name);
    if let Some(ref ri) = field.repeat {
        if ri.prefix.is_some() {
            return format!(
                "stream.write_unsigned_varint({}.len() as u32)?;\n        \
                 for item in &{} {{\n            {}\n        }}\n",
                self_field, self_field, write_single("item", &field.field_type, &field.enum_ref, schema));
        } else if ri.count.is_some() {
            return format!(
                "for item in &{} {{\n            {}\n        }}\n",
                self_field, write_single("item", &field.field_type, &field.enum_ref, schema));
        }
    }
    if let Some(ref tr) = field.type_repeat {
        if tr.prefix.is_some() {
            return format!(
                "stream.write_unsigned_varint({}.len() as u32)?;\n        \
                 for item in &{} {{\n            {}\n        }}\n",
                self_field, self_field, write_single("item", &field.field_type, &field.enum_ref, schema));
        }
    }
    write_single(&self_field, &field.field_type, &field.enum_ref, schema)
}

fn write_single(value_expr: &str, field_type: &FieldType, enum_ref: &Option<String>, schema: &Schema) -> String {
    let is_ref = value_expr == "item";
    match field_type {
        FieldType::Named(name) => {
            if enum_ref.is_some() {
                let v = if is_ref { format!("*{} as i32", value_expr) } else { format!("{} as i32", value_expr) };
                return format!("stream.write_varint({})?;", v);
            }
            if type_mapping::is_integer_alias(name) {
                let ty = type_mapping::integer_alias_type(name);
                let v = if is_ref { format!("*{}", value_expr) } else { value_expr.to_string() };
                return if ty == "i64" {
                    format!("stream.write_varint64({})?;", v)
                } else {
                    format!("stream.write_varint({})?;", v)
                };
            }
            if let Some(m) = type_mapping::primitive_mapping(name) {
                if m.write_call == "write_string" {
                    return format!("stream.write_string({})?;", format!("&{}", value_expr));
                }
                let v = if is_ref { format!("*{}", value_expr) } else { value_expr.to_string() };
                return format!("stream.{}({})?;", m.write_call, v);
            }
            if name == "CompoundTag" {
                return format!("stream.write_raw_bytes(&{}); /* CompoundTag */", value_expr);
            }
            // Type with invalid Rust name chars — treat as raw bytes
            if name.contains('<') || name.contains('>') || name.contains('(') || name.contains(')') {
                return format!("stream.write_raw_bytes(&{}); /* raw */", value_expr);
            }
            // Enum type — write as i32
            if schema.is_enum(name) {
                let v = if is_ref { format!("*{} as i32", value_expr) } else { format!("{} as i32", value_expr) };
                return format!("stream.write_varint({})?;", v);
            }
            // Unknown type (not in schema, not primitive) — treat as raw bytes
            if !schema.is_type(name) {
                return format!("stream.write_raw_bytes(&{}); /* unknown */", value_expr);
            }
            format!("{}.write_to(stream)?;", value_expr)
        }
        FieldType::SwitchCase(sc) => {
            let has_empty = sc.cases.iter().any(|c| matches!(c, bedrock_protocol_schema::field::SwitchCaseBranch::Empty));
            if has_empty {
                // Generate the correct write for the non-empty value
                let val_write = if let Some(non_empty) = sc.cases.iter().find(|c| !matches!(c, bedrock_protocol_schema::field::SwitchCaseBranch::Empty)) {
                    match non_empty {
                        bedrock_protocol_schema::field::SwitchCaseBranch::Primitive(s) => {
                            let wm = type_mapping::write_method(s);
                            if wm == "write_raw_bytes" {
                                "stream.write_raw_bytes(val)?;".to_string()
                            } else {
                                format!("stream.{}(*val)?;", wm)
                            }
                        }
                        bedrock_protocol_schema::field::SwitchCaseBranch::Type(s) => {
                            if s.contains('<') || s.contains('>') || s.contains('(') || s.contains(')') || s.contains(',') {
                                "stream.write_raw_bytes(val)?;".to_string()
                            } else {
                                "val.write_to(stream)?;".to_string()
                            }
                        }
                        _ => "stream.write_unsigned_varint(*val as u32)?;".to_string(),
                    }
                } else {
                    "stream.write_raw_bytes(val)?;".to_string()
                };
                return format!(
                    "if let Some(ref val) = {} {{\n            \
                     stream.write_bool(true)?;\n            \
                     {}\n        \
                     }} else {{\n            \
                     stream.write_bool(false)?;\n        \
                     }}\n",
                    value_expr, val_write);
            }
            format!("stream.write_raw_bytes(&{});", value_expr)
        }
        FieldType::Map { key, value } => {
            let kt = type_mapping::rust_primitive_type(key);
            // f32/f64 can't be HashMap keys — treat as raw bytes
            if kt == "f32" || kt == "f64" {
                return format!("stream.write_raw_bytes(&{});", value_expr);
            }
            // Determine how to write the value
            let value_call = if value.contains('<') || value.contains('>') || value.contains('(') || value.contains(')') {
                "stream.write_raw_bytes(v)?;".to_string()
            } else if schema.is_type(value) || schema.is_enum(value) {
                "v.write_to(stream)?;".to_string()
            } else {
                format!("stream.{}(v)?;", type_mapping::write_method(value))
            };
            let key_is_string = type_mapping::write_method(key) == "write_string";
            let key_expr = if key_is_string { "k".to_string() } else { format!("*k") };
            format!(
                "stream.write_unsigned_varint({}.len() as u32)?;\n        \
                 for (k, v) in &{} {{\n            \
                     stream.{}({})?;\n            \
                     {}\n        \
                 }}\n",
                value_expr, value_expr,
                type_mapping::write_method(key),
                key_expr,
                value_call,
            )
        }
    }
}

fn read_field(field: &bedrock_protocol_schema::FieldDefinition, schema: &Schema) -> String {
    if let Some(ref ri) = field.repeat {
        if ri.prefix.is_some() {
            let inner = read_single(&field.field_type, &field.enum_ref, schema);
            return format!(
                "{{\n            \
                 let mut items: Vec<_> = Vec::new();\n            \
                 let _count = stream.read_unsigned_varint()?;\n            \
                 for _ in 0.._count {{\n                \
                     let item = {};\n                \
                     items.push(item);\n            \
                 }}\n            \
                 items\n        }}", inner);
        } else if let Some(c) = ri.count {
            let inner = read_single(&field.field_type, &field.enum_ref, schema);
            return format!(
                "{{\n            \
                 let mut items = Vec::with_capacity({});\n            \
                 for _ in 0..{} {{\n                \
                     let item = {};\n                \
                     items.push(item);\n            \
                 }}\n            \
                 items\n        }}", c, c, inner);
        }
    }
    if let Some(ref tr) = field.type_repeat {
        if tr.prefix.is_some() {
            let inner = read_single(&field.field_type, &field.enum_ref, schema);
            return format!(
                "{{\n            \
                 let mut items: Vec<_> = Vec::new();\n            \
                 let _count = stream.read_unsigned_varint()?;\n            \
                 for _ in 0.._count {{\n                \
                     let item = {};\n                \
                     items.push(item);\n            \
                 }}\n            \
                 items\n        }}", inner);
        }
    }
    read_single(&field.field_type, &field.enum_ref, schema)
}

fn read_single(field_type: &FieldType, enum_ref: &Option<String>, schema: &Schema) -> String {
    match field_type {
        FieldType::Named(name) => {
            if let Some(enum_name) = enum_ref {
                let enum_type = naming::to_rust_enum_name(enum_name);
                return format!("<{}>::try_from(stream.read_varint()?)?", enum_type);
            }
            if type_mapping::is_integer_alias(name) {
                let ty = type_mapping::integer_alias_type(name);
                return if ty == "i64" {
                    "stream.read_varint64()?".to_string()
                } else {
                    "stream.read_varint()?".to_string()
                };
            }
            if name == "CompoundTag" {
                return "stream.read_remaining()? /* CompoundTag */".to_string();
            }
            if let Some(m) = type_mapping::primitive_mapping(name) {
                return format!("{}?", m.read_call);
            }
            let struct_name = naming::to_rust_struct_name(name);
            // Type name contains invalid Rust chars — treat as raw bytes
            if name.contains('<') || name.contains('>') || name.contains('(') || name.contains(')') {
                return "stream.read_remaining()?".to_string();
            }
            if schema.is_enum(name) {
                return format!("<{}>::try_from(stream.read_varint()?)?", struct_name);
            }
            if schema.is_type(name) {
                return format!("<{} as BedrockSerializable>::read_from(stream)?", struct_name);
            }
            "stream.read_remaining()?".to_string()
        }
        FieldType::SwitchCase(sc) => {
            let has_empty = sc.cases.iter().any(|c| matches!(c, bedrock_protocol_schema::field::SwitchCaseBranch::Empty));
            if has_empty {
                // Check the non-empty branch type
                let non_empty = sc.cases.iter().find(|c| !matches!(c, bedrock_protocol_schema::field::SwitchCaseBranch::Empty));
                let use_raw = non_empty.map_or(false, |c| match c {
                    bedrock_protocol_schema::field::SwitchCaseBranch::Type(s) =>
                        s.contains('<') || s.contains('>') || s.contains('(') || s.contains(')'),
                    _ => false,
                });
                if use_raw {
                    return format!(
                        "{{\n            \
                         if stream.read_bool()? {{\n                \
                             Some(stream.read_remaining()?)\n            \
                         }} else {{\n                \
                             None\n            \
                         }}\n        }}");
                }
                // Determine how to read the non-empty value
                let some_value = match non_empty {
                    Some(bedrock_protocol_schema::field::SwitchCaseBranch::Primitive(s)) => {
                        format!("stream.{}()? as i32", type_mapping::read_method(s))
                    }
                    Some(bedrock_protocol_schema::field::SwitchCaseBranch::Type(s)) => {
                        if schema.is_type(s) {
                            format!("<{} as BedrockSerializable>::read_from(stream)?", naming::to_rust_struct_name(s))
                        } else if schema.is_enum(s) {
                            format!("<{}>::try_from(stream.read_varint()?)?", naming::to_rust_struct_name(s))
                        } else {
                            "stream.read_remaining()?".to_string()
                        }
                    }
                    _ => "stream.read_unsigned_varint()? as i32".to_string(),
                };
                return format!(
                    "{{\n            \
                     if stream.read_bool()? {{\n                \
                         Some({})\n            \
                     }} else {{\n                \
                         None\n            \
                     }}\n        }}",
                    some_value);
            }
            "stream.read_remaining()?".to_string()
        }
        FieldType::Map { key, value } => {
            let kt = type_mapping::rust_primitive_type(key);
            // f32/f64 can't be HashMap keys — treat as raw bytes
            if kt == "f32" || kt == "f64" {
                return "stream.read_remaining()?".to_string();
            }
            let value_read = if value.contains('<') || value.contains('>') || value.contains('(') || value.contains(')') {
                "stream.read_remaining()?".to_string()
            } else if schema.is_type(value) {
                format!("<{} as BedrockSerializable>::read_from(stream)?", naming::to_rust_struct_name(value))
            } else if schema.is_enum(value) {
                format!("<{}>::try_from(stream.read_varint()?)?", naming::to_rust_struct_name(value))
            } else {
                format!("stream.{}()?", type_mapping::read_method(value))
            };
            format!(
                "{{\n            \
                 let mut map = std::collections::HashMap::new();\n            \
                 let _count = stream.read_unsigned_varint()?;\n            \
                 for _ in 0.._count {{\n                \
                     let k = stream.{}()?;\n                \
                     let v = {};\n                \
                     map.insert(k, v);\n            \
                 }}\n            \
                 map\n        }}",
                type_mapping::read_method(key),
                value_read)
        }
    }
}
