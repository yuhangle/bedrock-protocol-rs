use bedrock_protocol_schema::EnumDefinition;

use crate::naming;

/// Generate Rust source code for all enum definitions.
pub fn generate(enums: &[EnumDefinition]) -> String {
    let mut output = String::new();
    output.push_str("// This file is @generated by bedrock-codegen. Do not edit.\n\n");
    output.push_str("#![allow(non_camel_case_types, unused_imports, unused_must_use, unused_variables)]\n\n");

    for def in enums {
        output.push_str(&generate_enum(def));
        output.push('\n');
    }

    output
}

/// Generate a single Rust enum from an enum definition.
fn generate_enum(def: &EnumDefinition) -> String {
    let enum_name = naming::to_rust_enum_name(&def.name);
    let mut out = String::new();

    out.push_str(&format!(
        "#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\n"
    ));
    out.push_str(&format!("#[repr(i32)]\n"));
    out.push_str(&format!("pub enum {} {{\n", enum_name));

    let mut has_zero = false;
    let mut seen_values = std::collections::BTreeSet::new();
    for variant in &def.values {
        let variant_name = naming::sanitize_enum_variant(&variant.name);
        let value = variant.value;
        if !seen_values.insert(value) {
            continue;
        }
        if value == 0 { has_zero = true; }
        out.push_str(&format!("    {} = {},\n", variant_name, value));
    }

    out.push_str("}\n\n");

    // Default impl: use variant with value 0 if available, otherwise unsafe zeroed
    if has_zero {
        out.push_str(&format!(
            "impl Default for {} {{\n    fn default() -> Self {{\n        <Self>::try_from(0).unwrap()\n    }}\n}}\n\n",
            enum_name
        ));
    } else {
        out.push_str(&format!(
            "impl Default for {} {{\n    fn default() -> Self {{\n        unsafe {{ std::mem::zeroed() }}\n    }}\n}}\n\n",
            enum_name
        ));
    }

    // Generate TryFrom<i32> impl
    out.push_str(&format!(
        "impl TryFrom<i32> for {} {{\n",
        enum_name
    ));
    out.push_str("    type Error = bedrock_common::BinaryStreamError;\n");
    out.push_str("    fn try_from(value: i32) -> Result<Self, bedrock_common::BinaryStreamError> {\n");
    out.push_str("        match value {\n");
    let mut seen_try = std::collections::BTreeSet::new();
    for variant in &def.values {
        if !seen_try.insert(variant.value) {
            continue;
        }
        let variant_name = naming::sanitize_enum_variant(&variant.name);
        out.push_str(&format!(
            "            {} => Ok(Self::{}),\n",
            variant.value, variant_name
        ));
    }
    out.push_str(&format!(
        "            _ => Err(bedrock_common::BinaryStreamError::InvalidData {{\n"
    ));
    out.push_str(&format!(
        "                description: \"invalid {} value\",\n",
        enum_name
    ));
    out.push_str("            }),\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("}\n");

    // Generate From<Self> for i32
    out.push_str(&format!(
        "impl From<{}> for i32 {{\n",
        enum_name
    ));
    out.push_str(&format!("    fn from(value: {}) -> Self {{\n", enum_name));
    out.push_str("        value as i32\n");
    out.push_str("    }\n");
    out.push_str("}\n");

    out
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_generate_simple_enum() {
        let def = EnumDefinition {
            name: "TestEnum".to_string(),
            values: vec![
                bedrock_protocol_schema::EnumValue {
                    name: "First".to_string(),
                    value: 0,
                },
                bedrock_protocol_schema::EnumValue {
                    name: "Second".to_string(),
                    value: 1,
                },
            ],
        };
        let code = generate_enum(&def);
        assert!(code.contains("pub enum TestEnum {"));
        assert!(code.contains("First = 0"));
        assert!(code.contains("Second = 1"));
        assert!(code.contains("impl TryFrom<i32> for TestEnum"));
        assert!(code.contains("impl From<TestEnum> for i32"));
    }
}
