use bedrock_protocol_schema::Schema;

use crate::naming;

/// Generate Rust source for the MinecraftPacketIds enum from the schema.
pub fn generate(schema: &Schema) -> String {
    let mut output = String::new();

    output.push_str("// This file is @generated by bedrock-codegen. Do not edit.\n\n");
    output.push_str("#![allow(non_camel_case_types, unused_imports, unused_must_use, unused_variables)]\n\n");
    output.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]\n");
    output.push_str("#[repr(i32)]\n");
    output.push_str("pub enum MinecraftPacketIds {\n");

    for def in &schema.packets {
        let variant_name = enum_variant_name(&def.name, def.id);
        output.push_str(&format!("    {} = {},\n", variant_name, def.id));
    }

    // Sentinel value for unknown/unimplemented packet IDs
    let max_id = schema.packets.iter().map(|p| p.id).max().unwrap_or(0);
    output.push_str(&format!("    EndId = {},\n", max_id + 1));

    output.push_str("}\n\n");

    // Generate from_i32 impl
    output.push_str("impl MinecraftPacketIds {\n");
    output.push_str("    pub fn from_i32(value: i32) -> Option<Self> {\n");
    output.push_str("        use MinecraftPacketIds::*;\n");
    output.push_str("        Some(match value {\n");
    for def in &schema.packets {
        let variant_name = enum_variant_name(&def.name, def.id);
        output.push_str(&format!("            {} => {},\n", def.id, variant_name));
    }
    output.push_str("            _ => return None,\n");
    output.push_str("        })\n");
    output.push_str("    }\n");
    output.push_str("}\n");

    output
}

fn enum_variant_name(packet_name: &str, _id: u32) -> String {
    // Strip "Packet" suffix to get the variant name (e.g., "LoginPacket" -> "Login")
    let stripped = packet_name.strip_suffix("Packet").unwrap_or(packet_name);
    naming::to_rust_struct_name(stripped)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_enum_variant_name() {
        assert_eq!(enum_variant_name("LoginPacket", 1), "Login");
        assert_eq!(enum_variant_name("ContainerOpenPacket", 46), "ContainerOpen");
        assert_eq!(enum_variant_name("UpdateBlockPacket", 21), "UpdateBlock");
        // Packets that don't end in "Packet" keep the name as-is
        assert_eq!(enum_variant_name("EndId", 329), "EndId");
    }

    #[test]
    fn test_generate_from_schema() {
        let manifest_dir = std::path::Path::new(env!("CARGO_MANIFEST_DIR"));
        let docs_path = manifest_dir
            .parent()
            .and_then(|p| p.parent())
            .map(|p| p.join("docs").join("protocol-docs"))
            .expect("Cannot resolve protocol-docs path");
        let schema = bedrock_protocol_schema::Schema::load_all(&docs_path).expect("Failed to load schema");
        let code = generate(&schema);
        assert!(code.contains("Login = 1"));
        assert!(code.contains("ContainerOpen = 46"));
        assert!(code.contains("fn from_i32"));
        assert!(code.contains("_ => return None"));
    }
}
